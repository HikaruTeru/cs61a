def is_prime(n):
    """
    >>> is_prime(10)
    False
    >>> is_prime(7)
    True
    >>> is_prime(1) # one is not a prime number!!
    False
    """
    "*** YOUR CODE HERE ***"
    if n == 1:
        return False
    else:
        i = 1
        while n > i:
            if n % i == 0 and i != 1:
                return False
            else:
                i = i + 1
    return True

